                }
            ],
            '16-Apr': [
                {
                    title: 'Latihan Algoritma',
                    time: '08:00 - 10:00',
                    category: 'Tugas'
                },
                {
                    title: 'Belajar Flutter',
                    time: '13:00 - 15:00',
                    category: 'Proyek'
                }
            ],
            '17-Apr': [
                {
                    title: 'Pengerjaan Laporan',
                    time: '09:00 - 11:00',
                    category: 'Tugas'
                },
                {
                    title: 'Brainstorming Ide Startup',
                    time: '14:00 - 16:00',
                    category: 'Proyek'
                },
                {
                    title: 'Deploy Aplikasi',
                    time: '17:00 - 18:00',
                    category: 'Proyek'
                }
            ]
        };

        // Fungsi untuk menampilkan tugas berdasarkan tanggal
        function displayTasks(date) {
            const tasks = tasksData[date] || [];
            const tasksHeader = tasksContainer.querySelector('.tasks-header');
            let tasksHTML = '';

            // Update judul
            tasksHeader.querySelector('.tasks-title').textContent = `Tugas untuk ${date.replace('-', ' ')} 2025`;

            if (tasks.length === 0) {
                tasksHTML = '<div class="tasks-empty">Tidak ada tugas untuk tanggal ini</div>';
            } else {
                tasks.forEach(task => {
                    tasksHTML += `
                    <div class="task-item">
                        <div class="task-color" style="background-color: ${task.color};"></div>
                        <div class="task-info">
                            <div class="task-title">${task.title}</div>
                            <div class="task-time">${task.time}</div>
                        </div>
                        <div class="task-category">${task.category}</div>
                    </div>
                `;
                });
            }

            // Hapus tugas lama
            const oldTasks = tasksContainer.querySelectorAll('.task-item, .tasks-empty');
            oldTasks.forEach(task => task.remove());

            // Tambahkan tugas baru setelah header
            tasksHeader.insertAdjacentHTML('afterend', tasksHTML);
        }

        calendarDays.forEach(day => {
            day.addEventListener('click', function () {
                // Hapus kelas active dari semua hari
                calendarDays.forEach(d => d.classList.remove('active'));

                // Tambahkan kelas active ke hari yang dipilih
                this.classList.add('active');

                // Dapatkan tanggal yang dipilih
                const dayNumber = this.querySelector('.day-number').textContent;
                const dayName = this.querySelector('.day-name').textContent;
                const dateKey = `${dayNumber}-${dayName}`;

                // Tampilkan tugas untuk tanggal tersebut
                displayTasks(dateKey);
            });
        });

        // Tampilkan tugas untuk tanggal yang aktif saat halaman dimuat
        const activeDay = document.querySelector('.calendar-day.active');
        if (activeDay) {
            const dayNumber = activeDay.querySelector('.day-number').textContent;
            const dayName = activeDay.querySelector('.day-name').textContent;
            displayTasks(`${dayNumber}-${dayName}`);
        }

        // Untuk kategori pill
        const categoryPills = document.querySelectorAll('.category-pill');

        categoryPills.forEach(pill => {
            pill.addEventListener('click', function () {
                categoryPills.forEach(p => p.classList.remove('active'));
                this.classList.add('active');

                // Filter tugas berdasarkan kategori
                const category = this.textContent;
                const taskItems = document.querySelectorAll('.task-item');

                if (category === 'Semua') {
                    taskItems.forEach(item => item.style.display = 'flex');
                } else {
                    taskItems.forEach(item => {
                        const taskCategory = item.querySelector('.task-category').textContent;
                        item.style.display = (taskCategory === category) ? 'flex' : 'none';
                    });
                }
            });
        });
    });
